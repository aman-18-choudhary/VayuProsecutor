/** Local fuzzy search — used as fallback when backend /search is unavailable */
export function searchCitiesLocal(q: string): Array<{ name: string; display_name: string; lat: number; lon: number }> {
  if (!q || q.trim().length < 1) return [];
  const lower = q.toLowerCase().trim();
  return CITIES
    .filter(c => c.name.toLowerCase().includes(lower))
    .slice(0, 8)
    .map(c => ({ name: c.name, display_name: `${c.name}, India`, lat: c.lat, lon: c.lon }));
}
